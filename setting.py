import turtle
#도형 9개 그리기

#1
d1 = turtle.Turtle()
d1.hideturtle()
d1.speed(10000000)
d1.penup()
d1.goto(-150,-150)
d1.pendown()
for i in range(4):
    d1.forward(100)
    d1.left(90)
d1.penup()
d1.goto(1000,1000)

