import turtle
turtle.hideturtle()

#1번 채우기

def one1():
    d1 = turtle.Turtle()
    d1.ht( )
    d1.speed(10000000)
    d1.penup()
    d1.goto(-150,-150)
    d1.pendown()
    d1.pencolor("black")
    d1.begin_fill()
    for i in range(4):
        d1.forward(100)
        d1.left(90)
    d1.end_fill()
    d1.penup()
    d1.goto(1000,1000)

